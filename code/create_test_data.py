def get_metadata(data_file):
    with open(data_file, 'r') as f:
        for line in f:
            yield line


def spacing_punctuation(text):
    regular_punct = list(string.punctuation)
    for punc in regular_punct:
        if punc in text:
            text = text.replace(punc, f' {punc} ')
    return text.strip()


def clean_abstracts(abstract_texts):
    abstracts = list(abstract_texts)
    abstracts = map(str.lower, abstracts)
    abstracts = map(lambda x: re.sub(
        r'\$[^\$]\$', '', x), abstracts)  # Remove math
    abstracts = map(spacing_punctuation, abstracts)
    abstracts = map(lambda x: re.sub(
        r'\\\s+[a-z]+\s+\{([^}]+)\}', '\\1', x), abstracts)  # consolidate whitespace
    abstracts = map(lambda x: re.sub(r'\s+', ' ', x),
                    abstracts)  # consolidate whitespace
    abstracts = filter(lambda x: x.count(' ') < 150, abstracts)
    return abstracts


def get_abstracts(data_file, category):
    abstract_texts = set()
    for paper in tqdm.tqdm(get_metadata(data_file)):
        paper_dict = json.loads(paper)
        cats = paper_dict.get('categories').split(" ")
        abstract = paper_dict.get('abstract')
        if abstract not in abstract_texts and any(cat.startswith(category) for cat in cats):
            abstract_texts.add(abstract)
            yield paper_dict.get('abstract')


def write_abstracts(cleaned_abstracts, output_dir):
    train_abstracts, test_abstracts = train_test_split(
        list(cleaned_abstracts), test_size=0.1, random_state=42)
    with open(os.path.join(output_dir, "test.txt"), 'w') as f:
        f.writelines(map(lambda x: x + '\n', test_abstracts))
    with open(os.path.join(output_dir, "train.txt"), 'w') as f:
        f.writelines(map(lambda x: x + '\n', train_abstracts))


def create_dataset(data_file: str, target_category: str, output_dir: str):
    print('Reading from:', data_file)
    for category in CATEGORY_MAP:
        if category.startswith(target_category):
            print('Using category:', CATEGORY_MAP[category])
    print('Output dir:', output_dir)
    abstract_texts = get_abstracts(data_file, target_category)
    cleaned_abstracts = clean_abstracts(abstract_texts)
    write_abstracts(cleaned_abstracts, output_dir)


def main():
    parser = argparse.ArgumentParser()

    # Required parameters
    parser.add_argument("--category", default=None, type=str, required=True,
                        choices=CATEGORY_MAP.keys(),
                        help="The category to fine tune the model.")
    parser.add_argument("--arxiv-data-file", default=None, type=str,
                        help="The file in which the metadata is stored")
    parser.add_argument("--output-dir", default=None, type=str,
                        help="The directory in which to store the output")
    args = parser.parse_args()
    create_dataset(args.arxiv_data_file, args.category, args.output_dir)


if __name__ == "__main__":
    main()
